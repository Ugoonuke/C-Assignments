#include <stdio.h>
#include <stdlib.h>

int main()
{
    int integer1, integer2, integer3, integer4, integer5;

    int sum;

    printf("ENTER FIVE VARIABLES\n\n");
    scanf("%d%d%d%d%d", &integer1, &integer2, &integer3, &integer4, &integer5);

    sum = integer1 + integer2 + integer3 + integer4 + integer5;

    printf("The sum of %d and %d and %d and %d and %d is %d.\n", integer1, integer2, integer3, integer4, integer5, sum);
    return 0;
}
