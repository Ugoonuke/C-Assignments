#include <stdio.h>
#include <stdlib.h>

int main()
{
    int integer1;
    int note500;
    int note200;
    int note100;
    int note50;
    int note20;
    int note10;
    int note5;
    int poundcoin;
    int j;
    int k;
    int l;
    int n;
    int m;
    int o;
    int p;
    int q;
    int r;
    int s;
    int t;
    int u;
    int v;
    int w;
    int x;
    int y;
    int z;

    note500 = 500;
    note200 = 200;
    note100 = 100;
    note50 = 50;
    note20 = 20;
    note10 = 10;
    note5 = 5;
    poundcoin = 1;

    printf("Enter Your Wages Below...\n\n");
    scanf("%d",&integer1);

    t = integer1 / note500;
    t > 0;
    printf("You need:\n\n%d 500Euro Notes,\n", t);

    u = integer1 % note500;
    v = u / note200;
    v > 0;
    printf("%d 200Euro Notes,\n", v);

    w = u % note200;
    y = w / note100;
    y > 0;
    printf("%d 100Euro Notes,\n", y);

    l = w % note100;
    k = l / note50;
    k > 0;
    printf("%d 50Euro Notes,\n", k);

    j = l % note50;
    n = j / note20;
    n > 0;
    printf("%d 20Euro Notes,\n", n);

    m = j % note20;
    o = m / note10;
    o > 0;
    printf("%d 10Euro Notes,\n", o);

    p = m % note10;
    q = p / note5;
    q > 0;
    printf("%d 5Euro Notes,\n", q);

    r = p % note5;
    s = r / poundcoin;
    s >  0;
    printf("%d Euro Coins.", s);

}
